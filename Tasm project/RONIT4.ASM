DATA SEGMENT
DATABLOCK DB 01H,02H,03H,04H,05H
RESULT_LSB DB 0
RESULT_MSB DB 0
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
        MOV AX,DATA
        MOV DS,AX

        LEA SI,DATABLOCK
        MOV CL,04H
 UP:    MOV AL,[SI]
        
        JNC DOWN
        INC RESULT_MSB
 DOWN:  INC SI
        LOOP UP
CODE ENDS
END
